const paperApiBase = (
  window.__PAPER_API_URL__ ||
  import.meta.env.VITE_PAPER_API_URL ||
  ""
).replace(/\/$/, "");

export async function fetchPaperStatus(
  instrument = "ETH-USDT"
): Promise<PaperStatus> {
  if (window.__PAPER_STATUS__) return window.__PAPER_STATUS__;
  const response = await fetch(
    `${paperApiBase}/api/status?instrument=${encodeURIComponent(instrument)}`
  );
  if (!response.ok)
    throw new Error(`Paper API request failed: ${response.status}`);
  return response.json() as Promise<PaperStatus>;
}

export async function askMarketCopilot(
  question: string,
  instrument = "ETH-USDT"
): Promise<string> {
  const response = await fetch(`${paperApiBase}/api/chat`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ question, instrument }),
  });
  const payload = (await response.json()) as {
    answer?: string;
    error?: string;
  };
  if (!response.ok || !payload.answer)
    throw new Error(payload.error || "Copilot did not return an answer.");
  return payload.answer;
}

export async function fetchReplayItems(
  instrument: string
): Promise<ReplayItem[]> {
  const response = await fetch(
    `${paperApiBase}/api/replay?instrument=${encodeURIComponent(instrument)}`
  );
  if (!response.ok)
    throw new Error(`Replay request failed: ${response.status}`);
  const payload = (await response.json()) as { items?: ReplayItem[] };
  return payload.items || [];
}

export async function fetchReplayDetail(
  instrument: string,
  at: string
): Promise<ReplayDetail> {
  const response = await fetch(
    `${paperApiBase}/api/replay?instrument=${encodeURIComponent(
      instrument
    )}&at=${encodeURIComponent(at)}`
  );
  const payload = (await response.json()) as ReplayDetail & { error?: string };
  if (!response.ok || payload.error)
    throw new Error(
      payload.error || `Replay request failed: ${response.status}`
    );
  return payload;
}

export async function fetchOkxWatchlist(): Promise<WatchlistItem[]> {
  const wanted = ["BTC-USDT", "ETH-USDT", "SOL-USDT", "XRP-USDT", "DOGE-USDT"];
  return Promise.all(
    wanted.map(async (instrument) => {
      const response = await fetch(
        `https://www.okx.com/api/v5/market/ticker?instId=${instrument}`
      );
      if (!response.ok)
        throw new Error(
          `OKX ${instrument} ticker request failed: ${response.status}`
        );
      const payload = (await response.json()) as {
        data?: Array<{ last: string; open24h: string; vol24h: string }>;
      };
      const ticker = payload.data?.[0];
      if (!ticker)
        throw new Error(`OKX ${instrument} ticker response missing data`);
      const price = Number(ticker.last);
      const open = Number(ticker.open24h);
      return {
        instrument,
        price,
        volume: Number(ticker.vol24h),
        changePct: open ? ((price - open) / open) * 100 : 0,
      };
    })
  );
}

export function generateCandles(): Candle[] {
  const candles = [];
  let close = 2320;
  const start = Math.floor(Date.now() / 1000) - 86_400 * 70;

  for (let i = 0; i < 70; i += 1) {
    const drift = 3.2 + Math.sin(i / 5) * 10;
    const shock = Math.cos(i / 3) * 16;
    const open = close;
    close = Math.max(1850, close + drift + shock);
    const high = Math.max(open, close) + 18 + (i % 5) * 4;
    const low = Math.min(open, close) - 16 - (i % 4) * 5;
    candles.push({
      time: (start + i * 86_400) as UTCTimestamp,
      open: Number(open.toFixed(2)),
      high: Number(high.toFixed(2)),
      low: Number(low.toFixed(2)),
      close: Number(close.toFixed(2)),
    });
  }

  return candles;
}

