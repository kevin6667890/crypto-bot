async function fetchOkxSnapshot(
  instrument = "ETH-USDT"
): Promise<MarketSnapshot> {
  const [tickerResponse, candles] = await Promise.all([
    fetch(`https://www.okx.com/api/v5/market/ticker?instId=${instrument}`),
    fetchOkxCandles("15m", 25, instrument),
  ]);
  if (!tickerResponse.ok) {
    throw new Error(`OKX ticker request failed: ${tickerResponse.status}`);
  }
  const payload = (await tickerResponse.json()) as {
    data?: Array<{
      last: string;
      open24h: string;
      high24h: string;
      low24h: string;
      vol24h: string;
      ts: string;
    }>;
  };
  const data = payload.data?.[0];
  if (!data) throw new Error("OKX ticker response missing data");
  const price = Number(data.last);
  const open24h = Number(data.open24h);
  const closes = candles.map((candle) => candle.close);
  return {
    price,
    changePct: open24h > 0 ? ((price - open24h) / open24h) * 100 : 0,
    high24: Number(data.high24h),
    low24: Number(data.low24h),
    volume: Number(data.vol24h),
    ema20: closes.length >= 20 ? calculateEma(closes, 20) : null,
    updatedAt: formatTime(Number(data.ts)),
    source: "OKX",
  };
}

async function fetchOkxCandles(
  interval = "15m",
  limit = 160,
  instrument = "ETH-USDT"
): Promise<Candle[]> {
  const bar = normalizeOkxBar(interval);
  const rows = new Map<number, string[]>();
  let after: string | undefined;
  while (rows.size < limit) {
    const pageSize = Math.min(300, limit - rows.size);
    const endpoint = after ? "history-candles" : "candles";
    const cursor = after ? `&after=${after}` : "";
    const response = await fetch(
      `https://www.okx.com/api/v5/market/${endpoint}?instId=${instrument}&bar=${bar}&limit=${pageSize}${cursor}`
    );
    if (!response.ok)
      throw new Error(`OKX candles request failed: ${response.status}`);
    const payload = (await response.json()) as { data?: string[][] };
    if (!payload.data?.length) break;
    const previousSize = rows.size;
    payload.data.forEach((row) => rows.set(Number(row[0]), row));
    after = payload.data[payload.data.length - 1][0];
    if (rows.size === previousSize || payload.data.length < pageSize) break;
  }
  return [...rows.values()]
    .sort((a, b) => Number(a[0]) - Number(b[0]))
    .slice(-limit)
    .map((row) => ({
      time: Math.floor(Number(row[0]) / 1000) as UTCTimestamp,
      open: Number(row[1]),
      high: Number(row[2]),
      low: Number(row[3]),
      close: Number(row[4]),
      volume: Number(row[5]),
    }));
}

